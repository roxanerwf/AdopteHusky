    <h2>Comportement et caractère</h2>

    <p>Le Husky Sibérien est réputé pour être un chien têtu, difficile à éduquer. C’est toutefois un chien qui n’a pas une once d’agressivité et qui déborde de joie. Sa grande sociabilité l’empêche de devenir un chien de garde. S’il est un excellent compagnon de jeu pour les grands enfants, il préfère ignorer les tout jeunes humains. Par ailleurs, rapporter un bâton et jouer à la balle le désintéresse très vite.</p>

    <p>C'est un chien robuste et dynamique. Ses origines de chien de traîneaux lui confèrent une bonne résistance physique et mentale. C’est pourquoi, le Husky sibérien n’est pas forcément un chien “facile à vivre”, dans le sens où il peut se montrer déterminé.

    <br/>Ce chien plutôt sûr de lui donc, sera aussi relativement indépendant. Certains propriétaires de Husky vous diront qu’avoir un Husky est comme vivre avec un chat.</p>

    <p>De plus, son instinct de chasseur est particulièrement marqué et associé à un caractère plutôt indépendant, cela rendra principalement les exercices comme le rappel difficiles voire impossibles à mettre en place.<br/>

       Pour finir, votre jardin devra être impeccablement clôturé, car cette race de chien est très intelligente et comprendra vite comment obtenir plus de liberté en sautant votre clôture ou en creusant sous votre grillage.</p>

