<b>Ses oreilles :</b> triangulaires, et légèrement arrondies à la pointe, elles sont dressées, attachées haut et assez rapprochées.<br/><br/>
<b>Ses yeux :</b> légèrement obliques, elles sont en forme d’amande. Ils peuvent être bleus, marrons ou vairons.<br/><br/>
